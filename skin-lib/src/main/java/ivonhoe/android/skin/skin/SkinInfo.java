public class SkinInfo {

    private String name;
    private File archive;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public File getArchive() {
        return archive;
    }

    public void setArchive(File archive) {
        this.archive = archive;
    }
}
